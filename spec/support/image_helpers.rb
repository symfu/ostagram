module ImageHelpers
  def create_test_image_file(filename, content_type = 'image/jpeg')
    temp_file = Tempfile.new([filename, '.jpg'], binmode: true)
    temp_file.write(create_minimal_jpeg_data)
    temp_file.rewind

    Rack::Test::UploadedFile.new(temp_file.path, content_type)
  end

  def create_minimal_jpeg_data
    [
      0xFF, 0xD8, 0xFF, 0xE0, 0x00, 0x10, 0x4A, 0x46, 0x49, 0x46, 0x00, 0x01,
      0x01, 0x01, 0x00, 0x48, 0x00, 0x48, 0x00, 0x00, 0xFF, 0xDB, 0x00, 0x43,
      0x00, 0xFF, 0xC0, 0x00, 0x0B, 0x08, 0x00, 0x01, 0x00, 0x01, 0x01, 0x01,
      0x11, 0x00, 0xFF, 0xC4, 0x00, 0x14, 0x00, 0x01, 0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x08,
      0xFF, 0xDA, 0x00, 0x08, 0x01, 0x01, 0x00, 0x00, 0x3F, 0x00, 0x37, 0xFF,
      0xD9
    ].pack('C*')
  end

  def mock_image_processing
    if defined?(MiniMagick::Image)
      allow(MiniMagick::Image).to receive(:new).and_return(double(
        path: '/tmp/test_image.jpg',
        width: 100,
        height: 100,
        format: 'JPEG',
        valid?: true
      ))
    end
  end
end

RSpec.configure do |config|
  config.include ImageHelpers
end
